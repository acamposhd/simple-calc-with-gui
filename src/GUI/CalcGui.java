    private void percJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_percJbtnActionPerformed
        operator = "%";
        if ((!"".equals(outputJtxt.getText()) && outputJtxt.getText().substring(0, outputJtxt.getText().length() - 1).endsWith("n"))) {
            outputJtxt.setText(outputJtxt.getText().substring(0, outputJtxt.getText().length() - 4) + operator);
        } else {
            if ((!"".equals(outputJtxt.getText()) && outputJtxt.getText().substring(0, outputJtxt.getText().length()).endsWith("n"))) {
                outputJtxt.setText(inputJtxt.getText().substring(0, inputJtxt.getText().length()) + operator);
                numberOne = Float.parseFloat(inputJtxt.getText());
                inputJtxt.setText("0");
            } else {
                if ((!"".equals(outputJtxt.getText()) && outputJtxt.getText().substring(0, outputJtxt.getText().length()).endsWith("s"))) {
                    outputJtxt.setText(inputJtxt.getText().substring(0, inputJtxt.getText().length()) + operator);
                    numberOne = Float.parseFloat(inputJtxt.getText());
                    inputJtxt.setText("0");
                } else {
                    if ((!"".equals(outputJtxt.getText()) && outputJtxt.getText().endsWith("!"))) {
                        outputJtxt.setText(inputJtxt.getText() + operator);
                        numberOne = Float.parseFloat(inputJtxt.getText());
                        inputJtxt.setText("0");
                    } else {
                        if (!"".equals(outputJtxt.getText())) {
                            outputJtxt.setText(outputJtxt.getText().substring(0, outputJtxt.getText().length() - 1) + operator);
                        }
                    }
                }
            }
        }

        Validators.validateOperator(inputJtxt.getText(), outputJtxt.getText(), operator);
        Validators.bigValidator(input, output);
        setInput(input);
        setOutput(output);
        Operations.operator = "%";
        inputJtxt.requestFocus();
    }//GEN-LAST:event_percJbtnActionPerformed

