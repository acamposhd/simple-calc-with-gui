    private void elevatedNJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elevatedNJbtnActionPerformed
        operator = "(^n)";
        try {
            if (!"".equals(outputJtxt.getText())) {

                outputJtxt.setText(outputJtxt.getText().substring(0, outputJtxt.getText().length() - 4) + operator);
            }
            Validators.validateOperator(inputJtxt.getText(), outputJtxt.getText(), operator);
            Validators.bigValidator(input, output);
            this.setInput("0");
            this.setOutput(output);
            Operations.operator = "(^n)";
            Operations.elevatedToN(numberOne, Utilities.numberTwo);
            Validators.isEntire(Operations.C);
            Validators.checkInfinity();
            this.setInput("0");
            inputJtxt.requestFocus();

        } catch (StringIndexOutOfBoundsException outOf) {
            setInput(Validators.error);
            setOutput(Validators.error);
        }
    }//GEN-LAST:event_elevatedNJbtnActionPerformed

