    private void factorialJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_factorialJbtnActionPerformed
        operator = "!";
        try {
            if (!"".equals(outputJtxt.getText())) {
                outputJtxt.setText(outputJtxt.getText().substring(0, outputJtxt.getText().length() - 1) + operator);
            }
            Validators.validateOperator(inputJtxt.getText(), output, operator);
            Validators.bigValidator(input, "");
            this.setInput("0");
            this.setOutput(output);
            Operations.operator = "(!)";
            Operations.fact(numberOne);
            Validators.checkInfinity();
            if (input.equals(Validators.error) || Operations.C == 0) {
                inputJtxt.setText(input);
                outputJtxt.setText(input);
            } else {
                Validators.isEntire(Operations.C);
                this.setInput(Validators.print());
                inputJtxt.requestFocus();
            }
        } catch (StringIndexOutOfBoundsException outOf) {
            setInput(Validators.error);
            setOutput(Validators.error);
        }
    }//GEN-LAST:event_factorialJbtnActionPerformed

