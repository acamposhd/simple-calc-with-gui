    private void negativeJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_negativeJbtnActionPerformed

        if (inputJtxt.getText().equals("")) {
            Validators.validateSyntaxErrorNegative(inputJtxt.getText(), outputJtxt.getText(), "-");
            this.setInput(input);
            this.setOutput(output);
            inputJtxt.requestFocus();
            inputJtxt.setText("0");
        } else {
            inputJtxt.setText(inputJtxt.getText());
        }
        if (inputJtxt.getText().equals("0")) {
            inputJtxt.setText("-");
        } else {
            if (inputJtxt.getText().charAt(0) == '-') {
                inputJtxt.setText(inputJtxt.getText().substring(1, inputJtxt.getText().length()));
            } else {
                if (inputJtxt.getText().charAt(0) == '0' && inputJtxt.getText().charAt(1) != '.') {
                    inputJtxt.setText(inputJtxt.getText().replace("0", ""));
                }
                inputJtxt.setText('-' + inputJtxt.getText());
            }
        }

    }//GEN-LAST:event_negativeJbtnActionPerformed

    private void equalJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalJbtnActionPerformed

        if (inputJtxt.getText().equals("Infinity")) {
            setInput(Validators.error);
            setOutput(Validators.error);
        } else {
            if (inputJtxt.getText().equals("-")) {
                inputJtxt.setText("-");
            } else {

                boolean validator = Validators.emptyInput(inputJtxt.getText());
                if (validator == true) {
                    this.setInput(Validators.error);
                    this.setOutput(Validators.error);
                } else {
                    boolean empty = Validators.emptyOutput(outputJtxt.getText());
                    if (inputJtxt.getText().equals(Validators.error)) {
                        input = "";
                        output = "";
                        this.setInput(input);
                        this.setOutput(output);
                    } else {
                        if (empty == true && "(^n)".equals(operator)) {
                            numberOne = Float.parseFloat(inputJtxt.getText());
                            Utilities.numberTwo = Utilities.numberTwo;
                            Operations.elevatedToN(numberOne, Utilities.numberTwo);
                            Validators.isEntire(Operations.C);
                            this.setInput(Validators.print());
                            output = "";
                        } else {
                            empty = Validators.emptyOutput(outputJtxt.getText());
                            if (empty == true && !"%".equals(operator)) {
                                Utilities.numberTwo = numberOne;
                                numberOne = Float.parseFloat(inputJtxt.getText());
                                this.setOutput("");
                            } else {
                                Utilities.numberTwo = (Float.parseFloat(inputJtxt.getText()));
                                Validators.isEntire(Utilities.numberTwo);
                                this.setOutput("");
                            }
                        }
                        switch (operator) {

                            case "+":

                                Operations.add(numberOne, Utilities.numberTwo);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);
                                } else {
                                    this.setInput(Validators.print());
                                    numberOne = Utilities.numberTwo;
                                    Utilities.numberTwo = Float.parseFloat(inputJtxt.getText());
                                }
                                break;

                            case "-":
                                Operations.subs(numberOne, Utilities.numberTwo);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);
                                } else {
                                    this.setInput(Validators.print());
                                    numberOne = Utilities.numberTwo;
                                    Utilities.numberTwo = Float.parseFloat(inputJtxt.getText());
                                }
                                break;
                            case "*":
                                Operations.mult(numberOne, Utilities.numberTwo);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);
                                } else {
                                    this.setInput(Validators.print());
                                    numberOne = Utilities.numberTwo;
                                    Utilities.numberTwo = Float.parseFloat(inputJtxt.getText());
                                }
                                break;
                            case "/":
                                if (Utilities.numberTwo == 0) {
                                    Validators.secondNumberIsZero(String.valueOf(Utilities.numberTwo));
                                    setInput(input);
                                    setOutput(output);
                                } else {

                                    Operations.div(numberOne, Utilities.numberTwo);
                                    Validators.isEntire(Operations.C);
                                    Validators.checkInfinity();
                                    if (input.equals(Validators.error)) {
                                        inputJtxt.setText(input);
                                        outputJtxt.setText(input);
                                    } else {
                                        this.setInput(Validators.print());
                                        numberOne = Utilities.numberTwo;
                                        Utilities.numberTwo = Float.parseFloat(inputJtxt.getText());
                                    }
                                }
                                break;
                            case "%":
                                Operations.perc(numberOne, Utilities.numberTwo);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "SQRT":
                                Operations.sqrt(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "CUBICRT":
                                Operations.cubicRoot(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "(^2)":
                                Operations.elevatedSquare(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "(^3)":
                                Operations.elevatedCube(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "(^n)":
                                Operations.elevatedToN(numberOne, Utilities.numberTwo);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                output = "";
                                break;
                            case "sin":
                                Operations.sin(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "cos":
                                Operations.cos(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "tan":
                                Operations.tan(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            case "!":
                                Operations.fact(numberOne);
                                Validators.isEntire(Operations.C);
                                Validators.checkInfinity();
                                if (input.equals(Validators.error)) {
                                    inputJtxt.setText(input);
                                    outputJtxt.setText(input);

                                } else {
                                    this.setInput(Validators.print());
                                }
                                break;
                            default:
                                Validators.checkInfinity();
                                this.setInput(input);
                        }
                    }
                }

            }
        }
        Validators.checkInfinity();
        inputJtxt.requestFocus();
    }//GEN-LAST:event_equalJbtnActionPerformed

