    private void eraseJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eraseJbtnActionPerformed

        Validators.emptyInput(inputJtxt.getText());
        if (Validators.emptyInput == true) {
            this.setInput("0");

        } else {
            if (!"0".equals(inputJtxt.getText())) {
                this.setInput(inputJtxt.getText().substring(0, inputJtxt.getText().length() - 1));

                if ("".equals(inputJtxt.getText())) {
                    this.setInput("0");
                }
            } else {
                this.setInput("0");
            }
        }
        inputJtxt.requestFocus();
    }//GEN-LAST:event_eraseJbtnActionPerformed

