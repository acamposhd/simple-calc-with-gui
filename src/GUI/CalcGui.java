    private void inputJtxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputJtxtKeyTyped
        Validators.keyTyped(evt);
        char keyPressed = evt.getKeyChar();
        if ("dev".equals(Operations.operator)) {
            evt.consume();
        } else {
            switch (keyPressed) {

                case '0':
                    zeroJbtn.doClick();
                    break;
                case '1':
                    oneJbtn.doClick();
                    break;
                case '2':
                    twoJbtn.doClick();
                    break;
                case '3':
                    threeJbtn.doClick();
                    break;
                case '4':
                    fourJbtn.doClick();
                    break;
                case '5':
                    fiveJbtn.doClick();
                    break;
                case '6':
                    sixJbtn.doClick();
                    break;

                case '7':
                    sevenJbtn.doClick();
                    break;
                case '8':
                    eightJbtn.doClick();
                    break;
                case '9':
                    nineJbtn.doClick();
                    break;
                case '+':
                    addJbtn.doClick();
                    break;
                case '-':
                    subsJbtn.doClick();
                    break;
                case '*':
                    multJbtn.doClick();
                    break;
                case '/':
                    divJbtn.doClick();
                    break;
                case '%':
                    percJbtn.doClick();
                    break;
                case '=':
                    equalJbtn.doClick();
                    break;
                case '.':
                    dotJbtn.doClick();
                    break;
                case '_':
                    negativeJbtn.doClick();
                    break;
                case KeyEvent.VK_ENTER:
                    equalJbtn.doClick();
                    break;
                case KeyEvent.VK_DELETE:
                    clearJbtn.doClick();
                    break;
//            case KeyEvent.VK_BACK_SPACE:
//                eraseJbtn.doClick();
//                break;
                default:

            }
        }

    }//GEN-LAST:event_inputJtxtKeyTyped


